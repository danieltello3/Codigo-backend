numero1 = 10
numero2 = 20

#si las dos variables son enteras, todos los resultados a excepcion de la division seran enteros

# +
print(f"la suma es {numero1 + numero2}")
# -
print(f"la resta es {numero2 - numero1}")
# *
print(f"la multiplicacion es {numero1 * numero2}")
# /
print(f"la division es {numero1 / numero2}")
# % MODULO
print(f"el modulo es {numero2 % numero1}")
# // COCIENTE
print(f"el cociente es {numero2 // numero1}")